package setup

import (
	"fmt"
	"os"
	"path/filepath"
)

const workflowREADMEContent = `<!-- This file is auto-generated by compozy setup. Do not edit. -->

# Compozy Workflow Directory

This directory is managed by [Compozy](https://docs.compozy.ai), an AI-assisted
development workflow orchestrator.

## Quick Start

` + "```" + `bash
# Install bundled skills for your agents
compozy setup

# Create a PRD from an idea
compozy start --mode prd-tasks --name my-feature

# Execute PRD task files through AI agents
compozy start --name my-feature

# Run a single ad hoc prompt
compozy exec --prompt "refactor the auth module"
` + "```" + `

## Commands

| Command             | Description                                                  |
| ------------------- | ------------------------------------------------------------ |
| ` + "`" + `compozy setup` + "`" + `         | Install bundled public skills for supported agents           |
| ` + "`" + `compozy upgrade` + "`" + `       | Update the CLI to the latest release                         |
| ` + "`" + `compozy start` + "`" + `         | Execute PRD task files                                       |
| ` + "`" + `compozy exec` + "`" + `          | Execute one ad hoc prompt through the shared ACP runtime     |
| ` + "`" + `compozy fetch-reviews` + "`" + ` | Fetch provider review comments into a review round directory |
| ` + "`" + `compozy fix-reviews` + "`" + `   | Process review issue files from a specific review round      |
| ` + "`" + `compozy sync` + "`" + `          | Refresh task workflow metadata files                         |
| ` + "`" + `compozy archive` + "`" + `       | Move fully completed workflows into _archived                |
| ` + "`" + `compozy validate-tasks` + "`" + `| Validate task metadata under .compozy/tasks                  |
| ` + "`" + `compozy migrate` + "`" + `       | Convert legacy workflow artifacts to frontmatter             |

Run ` + "`" + `compozy <command> --help` + "`" + ` for detailed usage of any command.
`

// WriteWorkflowREADME creates or overwrites the README.md file inside the
// given compozy workflow directory (typically .compozy/).
func WriteWorkflowREADME(compozyDir string) error {
	if err := os.MkdirAll(compozyDir, 0o755); err != nil {
		return fmt.Errorf("create compozy directory %s: %w", compozyDir, err)
	}

	readmePath := filepath.Join(compozyDir, "README.md")
	if err := os.WriteFile(readmePath, []byte(workflowREADMEContent), 0o600); err != nil {
		return fmt.Errorf("write workflow README %s: %w", readmePath, err)
	}
	return nil
}
