package setup

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
)

func TestWriteWorkflowREADME(t *testing.T) {
	t.Parallel()

	t.Run("creates README in existing directory", func(t *testing.T) {
		t.Parallel()

		dir := t.TempDir()
		compozyDir := filepath.Join(dir, ".compozy")
		if err := os.MkdirAll(compozyDir, 0o755); err != nil {
			t.Fatalf("create dir: %v", err)
		}

		if err := WriteWorkflowREADME(compozyDir); err != nil {
			t.Fatalf("WriteWorkflowREADME: %v", err)
		}

		readmePath := filepath.Join(compozyDir, "README.md")
		content, err := os.ReadFile(readmePath)
		if err != nil {
			t.Fatalf("read README: %v", err)
		}

		if len(content) == 0 {
			t.Fatal("README is empty")
		}
		if !strings.Contains(string(content), "auto-generated by compozy setup") {
			t.Error("README missing auto-generated header")
		}
	})

	t.Run("creates directory if it does not exist", func(t *testing.T) {
		t.Parallel()

		dir := t.TempDir()
		compozyDir := filepath.Join(dir, ".compozy")

		if err := WriteWorkflowREADME(compozyDir); err != nil {
			t.Fatalf("WriteWorkflowREADME: %v", err)
		}

		readmePath := filepath.Join(compozyDir, "README.md")
		if _, err := os.Stat(readmePath); err != nil {
			t.Fatalf("README not created: %v", err)
		}
	})

	t.Run("overwrites existing README", func(t *testing.T) {
		t.Parallel()

		dir := t.TempDir()
		compozyDir := filepath.Join(dir, ".compozy")
		if err := os.MkdirAll(compozyDir, 0o755); err != nil {
			t.Fatalf("create dir: %v", err)
		}

		readmePath := filepath.Join(compozyDir, "README.md")
		if err := os.WriteFile(readmePath, []byte("old content"), 0o600); err != nil {
			t.Fatalf("write old README: %v", err)
		}

		if err := WriteWorkflowREADME(compozyDir); err != nil {
			t.Fatalf("WriteWorkflowREADME: %v", err)
		}

		content, err := os.ReadFile(readmePath)
		if err != nil {
			t.Fatalf("read README: %v", err)
		}

		if strings.Contains(string(content), "old content") {
			t.Error("README was not overwritten")
		}
		if !strings.Contains(string(content), "auto-generated by compozy setup") {
			t.Error("README missing auto-generated header after overwrite")
		}
	})
}

func TestWorkflowREADMEContentCoversAllCommands(t *testing.T) {
	t.Parallel()

	commands := []string{
		"compozy setup",
		"compozy upgrade",
		"compozy start",
		"compozy exec",
		"compozy fetch-reviews",
		"compozy fix-reviews",
		"compozy sync",
		"compozy archive",
		"compozy validate-tasks",
		"compozy migrate",
	}

	for _, cmd := range commands {
		if !strings.Contains(workflowREADMEContent, cmd) {
			t.Errorf("README content missing command %q", cmd)
		}
	}
}
